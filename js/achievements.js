window.unlockedAchievements = [];

window.checkAchievements = function() {
    let newUnlocks = [];
    
    Object.values(window.ACHIEVEMENTS).forEach(ach => {
        if (window.unlockedAchievements.includes(ach.id)) return;
        
        let unlocked = false;
        
        // Verificar según categoría
        switch(ach.category) {
            case 'clicks':
                unlocked = window.gameState?.totalClicks >= ach.requirement;
                break;
            case 'cps':
                unlocked = window.gameState?.cps >= ach.requirement;
                break;
            case 'autoClickers':
                unlocked = window.gameState?.autoClickers >= ach.requirement;
                break;
            case 'multiplier':
                unlocked = window.gameState?.clickMultiplier >= ach.requirement;
                break;
            case 'prestige':
                unlocked = window.prestigeData?.level >= ach.requirement;
                break;
            case 'minigames':
                unlocked = (window.minigameCount || 0) >= ach.requirement;
                break;
            case 'missions':
                unlocked = (window.missionCompleteCount || 0) >= ach.requirement;
                break;
            case 'events':
                unlocked = (window.eventParticipationCount || 0) >= ach.requirement;
                break;
            case 'time':
                unlocked = window.gameState?.playTime >= ach.requirement;
                break;
            case 'shop':
                unlocked = window.gameState?.totalPurchases >= ach.requirement;
                break;
            case 'secret':
                unlocked = checkSecretAchievement(ach.id);
                break;
        }
        
        if (unlocked) {
            window.unlockedAchievements.push(ach.id);
            if (window.gameState) {
                window.gameState.clickCount += ach.reward;
            }
            newUnlocks.push(ach);
        }
    });
    
    if (newUnlocks.length > 0) {
        newUnlocks.forEach(ach => showAchievementPopup(ach));
        if (window.updateUI) window.updateUI();
        if (window.saveGame) window.saveGame();
    }
    
    return newUnlocks;
};

function checkSecretAchievement(achId) {
    switch(achId) {
        case 'SECRET_1':
            return window.gameState?.cps >= 1000;
        case 'SECRET_2':
            return (window.rouletteJackpotStreak || 0) >= 3;
        case 'SECRET_3':
            return window.gameState?.totalClicks >= 1000000;
        case 'SECRET_4':
            return window.prestigeData?.level >= 100;
        case 'SECRET_5':
            return (window.eventStreak || 0) >= 10;
        case 'SECRET_6':
            return window.unlockedAchievements.length >= 50;
        case 'SECRET_7':
            return window.unlockedAchievements.length >= Object.keys(window.ACHIEVEMENTS).length;
        case 'SECRET_8':
            return window.gameState?.playTime >= 31536000;
        default:
            return false;
    }
}

function showAchievementPopup(achievement) {
    const popup = document.createElement('div');
    popup.className = 'achievement-popup';
    const rarityColor = window.RARITY_CONFIG[achievement.rarity]?.color || '#ffffff';
    popup.style.borderLeft = `4px solid ${rarityColor}`;
    popup.style.background = 'linear-gradient(135deg, rgba(0,0,0,0.95), rgba(0,0,0,0.9))';
    popup.style.position = 'fixed';
    popup.style.bottom = '20px';
    popup.style.right = '20px';
    popup.style.display = 'flex';
    popup.style.alignItems = 'center';
    popup.style.gap = '15px';
    popup.style.padding = '15px 20px';
    popup.style.borderRadius = '12px';
    popup.style.zIndex = '10000';
    popup.style.animation = 'slideInRight 0.5s, fadeOut 0.5s 4.5s';
    popup.style.maxWidth = '350px';
    popup.style.backdropFilter = 'blur(10px)';
    popup.style.boxShadow = `0 0 20px ${rarityColor}`;
    
    popup.innerHTML = `
        <div style="width: 50px; height: 50px; border-radius: 50%; background: ${rarityColor}20; display: flex; align-items: center; justify-content: center; font-size: 2rem;">
            ${achievement.icon}
        </div>
        <div style="flex: 1;">
            <div style="font-weight: bold; color: white; font-size: 1.1rem;">${achievement.name}</div>
            <div style="color: ${rarityColor}; font-size: 0.9rem;">${achievement.description}</div>
            <div style="color: gold; font-weight: bold; margin-top: 5px;">+${achievement.reward} clics</div>
        </div>
    `;
    
    document.body.appendChild(popup);
    
    setTimeout(() => {
        popup.remove();
    }, 5000);
}

